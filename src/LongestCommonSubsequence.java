    static int LCS(char x[], char y[], int l1, int l2) {                // time complexity is exponential
        if (l1 == -1 || l2 == -1) return 0;
        else if (x[l1] == y[l2])
            return 1 + LCS(x, y, l1 - 1, l2 - 1);
        else return Math.max(LCS(x, y, l1 - 1, l2), LCS(x, y, l1, l2 - 1));
    }

    static int lcs(char x[], char y[], int l1, int l2) {               //time complexity is (l1*l2)
        int ans[][] = new int[l1 + 1][l2 + 1];

        for (int i = 0; i <= l1; i++) {
            for (int j = 0; j <= l2; j++) {
                if (i == 0 || j == 0)
                    ans[i][j] = 0;
                else if (x[i - 1] == y[j - 1])
                    ans[i][j] = 1 + ans[i - 1][j - 1];
                else
                    ans[i][j] = Math.max(ans[i - 1][j], ans[i][j - 1]);
            }
        }
        return ans[l1][l2];
    }
